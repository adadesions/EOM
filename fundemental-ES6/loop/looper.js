//                                 DO-WHILE-Statement
// =============================================================================
console.log('===================================DO-WHILE========================================')
{
  let i = 0
  do{
    console.log(i+": ")
    condition.zeroEvenOdd(i)
    i++
  }while(i < 10)
}
//                                 WHILE-Statement
// =============================================================================
console.log('===================================WHILE========================================')
{
  let i = 0
  while(i < 10){
    console.log(i+": ")
    condition.zeroEvenOdd(i)
    i++
  }
}

//                                 FOR-IN-Statement
// =============================================================================
console.log('===================================FOR-IN========================================')
{
  let obj = {course:'EOM', provider:'AdaCode', date:'12-13 Sep 2015'}
  for(let x in obj){
    console.log(x +" : "+ obj[x])
  }
}

