//primative operator in js
'use strict'
// '=' equal for assign a value
{
  let x = 23
  console.log(x)
}


// '+=', '-=', '*=', '/=' , '%='
{
  let x = 23
      x += 23
  console.log(x)
}

//String operation
{
  let firstname = 'Ada',
      lastname  = 'Code',
      suffix    = '.I/O'

  console.log(firstname+lastname+suffix)
}

//String vs Number
{
  let x = 23,
      y = '13',
      z = 'AdaCode.io'

  console.log(x+y)
  console.log(x+z+y)
}
